// Translation utility functions
class TranslationManager {
    constructor() {
        this.currentLanguage = this.getStoredLanguage() || this.detectBrowserLanguage() || 'pt-BR';
        this.translations = translations;
        this.observers = [];
    }
    
    getStoredLanguage() {
        return localStorage.getItem('cambiomax_language');
    }
    
    detectBrowserLanguage() {
        const browserLang = navigator.language || navigator.userLanguage;
        const supportedLanguages = Object.keys(this.translations);
        
        // Check for exact match
        if (supportedLanguages.includes(browserLang)) {
            return browserLang;
        }
        
        // Check for language code match (e.g., 'en' matches 'en-US')
        const langCode = browserLang.split('-')[0];
        const match = supportedLanguages.find(lang => lang.startsWith(langCode));
        
        return match || 'pt-BR';
    }
    
    setLanguage(language) {
        if (this.translations[language]) {
            this.currentLanguage = language;
            localStorage.setItem('cambiomax_language', language);
            this.updatePageTranslations();
            this.notifyObservers(language);
        }
    }
    
    getCurrentLanguage() {
        return this.currentLanguage;
    }
    
    translate(key, fallback = key) {
        const translation = this.translations[this.currentLanguage];
        return translation && translation[key] ? translation[key] : fallback;
    }
    
    updatePageTranslations() {
        // Update all elements with data-translate attribute
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(element => {
            const key = element.getAttribute('data-translate');
            const translation = this.translate(key);
            
            if (element.tagName === 'INPUT' && element.type === 'text') {
                element.placeholder = translation;
            } else {
                element.textContent = translation;
            }
        });
        
        // Update placeholder attributes
        const placeholderElements = document.querySelectorAll('[data-translate-placeholder]');
        placeholderElements.forEach(element => {
            const key = element.getAttribute('data-translate-placeholder');
            const translation = this.translate(key);
            element.placeholder = translation;
        });
        
        // Update document title
        const titleKey = document.documentElement.getAttribute('data-translate-title');
        if (titleKey) {
            document.title = this.translate(titleKey);
        }
        
        // Update meta description
        const metaDescription = document.querySelector('meta[name="description"]');
        const descriptionKey = metaDescription?.getAttribute('data-translate');
        if (descriptionKey) {
            metaDescription.content = this.translate(descriptionKey);
        }
    }
    
    addObserver(callback) {
        this.observers.push(callback);
    }
    
    removeObserver(callback) {
        this.observers = this.observers.filter(obs => obs !== callback);
    }
    
    notifyObservers(language) {
        this.observers.forEach(callback => callback(language));
    }
    
    getAvailableLanguages() {
        return Object.keys(this.translations);
    }
    
    getLanguageInfo(langCode) {
        const languageNames = {
            'pt-BR': { name: 'Português', flag: 'br', nativeName: 'Português' },
            'en-US': { name: 'English', flag: 'us', nativeName: 'English' },
            'es-ES': { name: 'Español', flag: 'es', nativeName: 'Español' },
            'zh-CN': { name: 'Chinese', flag: 'cn', nativeName: '中文' },
            'th-TH': { name: 'Thai', flag: 'th', nativeName: 'ไทย' },
            'fr-FR': { name: 'French', flag: 'fr', nativeName: 'Français' }
        };
        
        return languageNames[langCode] || { name: langCode, flag: 'us', nativeName: langCode };
    }
}

// Create global translation manager instance
window.translationManager = new TranslationManager();

// Auto-initialize translations when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    window.translationManager.updatePageTranslations();
});

